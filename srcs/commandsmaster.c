#include"../inc/minishell.h"

void    commands(t_lists *lists, char **envp)
{
    int pid;

    pid = fork();
    if (pid == 0)
    {
        builtins(lists, envp);
        exit(0);
    }
    waitpid(pid, 0, 0);
}
